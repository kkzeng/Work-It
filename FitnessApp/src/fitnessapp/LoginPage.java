    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        user = txt_user.getText();
        pass = txt_password.getText();
        String check = "[" + user + ", " + pass + "]";
        try {
            client = new CSVReader(new FileReader(gdrivePath +"Java Files/clients.csv"), ',', '"', 1);
           
        } catch (FileNotFoundException ex) {
            Logger.getLogger(LoginPage.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        String[] nextLine;
        try {

            while ((nextLine = client.readNext()) != null) {
                if (nextLine != null) {
                    //Verifying the read data here
                    if (Arrays.toString(nextLine).equals(check)) {
                        verify = 1;
                    }

                }
            }

            if (verify == 1) {
                new mainScreen(user,gdrivePath).setVisible(true);
                dispose();

            } else {
                JOptionPane.showMessageDialog(null,
                        "You have entered a wrong password/username. Please make sure that you have not entered any spaces and try again. \nIf you are a new user and wish to register, please contact your trainer who will help you to register.");
            }
        } catch (IOException ex) {
            Logger.getLogger(LoginPage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

